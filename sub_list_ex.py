spam=['cat','bat','rat','elephant']
print(spam[0:4])
#yields=>['cat', 'bat', 'rat', 'elephant']
 
print(spam[1:3])
#yields ['bat', 'rat'] sublist slice will go up to but not include 2nd number
 
print(spam[0:-1])
#yeilds ['cat', 'bat', 'rat']
 
print(spam[0:-2])
#yeilds ['cat', 'bat']
 
 
